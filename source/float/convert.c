// バイナリ文字列を32ビット整数に変換
uint32_t binary_to_uint32(const char *binary) {
    uint32_t value = 0;
    for (int i = 0; i < 32; i++) {
        value <<= 1;
        if (binary[i] == '1') {
            value |= 1;
        }
    }
    return value;
}

// 32ビット整数をfloatに変換
float uint32_to_float(uint32_t value) {
    float result;
    memcpy(&result, &value, sizeof(float));
    return result;
}

